export function getCategoryById(id) {
  return categories.find(c => c.id === id) || categories[0]
}

export function getPartById(id) {
  return parts.find(p => p.id === Number(id))
}

export function getPartsByCategory(catId) {
  if (!catId || catId === 'all') return parts
  return parts.filter(p => p.category === catId)
}

export function formatPrice(price) {
  return new Intl.NumberFormat('ru-RU', { style: 'currency', currency: 'RUB', maximumFractionDigits: 0 }).format(price)
}

export function formatMileage(km) {
  return new Intl.NumberFormat('ru-RU').format(km) + ' км'
}
